function transliterate() {
    var textInput = document.getElementById("textInput");
    var outputDiv = document.getElementById("outputDiv");
    var text = textInput.value;

    var transliteratedText = "";
    for (var i = 0; i < text.length; i++) {
        var char = text.charAt(i);
        if (char in transliterationMap) {
            transliteratedText += String.fromCodePoint(transliterationMap[char]);
        } 
        else {
            transliteratedText += char;
        }
    }

    outputDiv1.innerText = transliteratedText;
    outputDiv2.innerText = transliteratedText;
}


